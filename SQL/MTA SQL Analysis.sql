Linear Attribution
==========================================================*/
-- Linear Attribution Model
CREATE OR REPLACE VIEW LINEAR_ATTRIBUTION AS
SELECT
	USER_ID,
	SOURCE,
	MEDIUM,
	CAMPAIGN_ID,
	CAMPAIGN_NAME,
	CONVERSION_ID,
	CONVERSION_TIMESTAMP,
	REVENUE,
	COUNT(*) OVER (
		PARTITION BY
			USER_ID
	) AS TOTAL_TOUCHES,
	1.0 / COUNT(*) OVER (
		PARTITION BY
			USER_ID
	) AS ATTRIBUTION_WEIGHT,
	REVENUE * (
		1.0 / COUNT(*) OVER (
			PARTITION BY
				USER_ID
		)
	) AS ATTRIBUTED_REVENUE
FROM
	USER_JOURNEY;

SELECT
	*
FROM
	LINEAR_ATTRIBUTION
LIMIT
	15;

SELECT
	SOURCE,
	COUNT(*) AS ATTRIBUTED_TOUCHPOINTS,
	ROUND(SUM(ATTRIBUTED_REVENUE), 2) AS ATTRIBUTED_REVENUE
FROM
	LINEAR_ATTRIBUTION
GROUP BY
	SOURCE
ORDER BY
	ATTRIBUTED_REVENUE DESC;

-- Linear Attribution by Campaign
CREATE OR REPLACE VIEW LINEAR_CAMPAIGN_REVENUE AS
SELECT
	CAMPAIGN_ID,
	CAMPAIGN_NAME,
	ROUND(SUM(ATTRIBUTED_REVENUE), 2) AS ATTRIBUTED_REVENUE,
	COUNT(DISTINCT CONVERSION_ID) AS ATTRIBUTED_CONVERSIONS
FROM
	LINEAR_ATTRIBUTION
WHERE
	CAMPAIGN_ID IS NOT NULL
GROUP BY
	CAMPAIGN_ID,
	CAMPAIGN_NAME;

SELECT
	*
FROM
	LINEAR_CAMPAIGN_REVENUE
ORDER BY
	ATTRIBUTED_REVENUE DESC
LIMIT
	10;

