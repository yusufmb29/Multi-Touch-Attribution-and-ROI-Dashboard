-- Last-Touch Attribution Model
-- Asks "Who closed the sale?"
CREATE OR REPLACE VIEW LAST_TOUCH_ATTRIBUTION AS
WITH
	JOURNEY_RANKED AS (
		SELECT
			USER_ID,
			SOURCE,
			MEDIUM,
			CAMPAIGN_ID,
			CAMPAIGN_NAME,
			CONVERSION_ID,
			CONVERSION_TIMESTAMP,
			REVENUE,
			ROW_NUMBER() OVER (
				PARTITION BY
					USER_ID
				ORDER BY
					TIMESTAMP DESC
			) AS TOUCH_ORDER
		FROM
			USER_JOURNEY
	)
SELECT
	*
FROM
	JOURNEY_RANKED
WHERE
	TOUCH_ORDER = 1;

SELECT
	*
FROM
	LAST_TOUCH_ATTRIBUTION;

SELECT
	COUNT(*)
FROM
	LAST_TOUCH_ATTRIBUTION;

SELECT
	SOURCE,
	COUNT(*) AS CONVERSIONS,
	SUM(REVENUE) AS ATTRIBUTED_REVENUE
FROM
	LAST_TOUCH_ATTRIBUTION
GROUP BY
	SOURCE
ORDER BY
	ATTRIBUTED_REVENUE ASC;

