-- Spend by Campaign View
CREATE OR REPLACE VIEW CAMPAIGN_SPEND AS
SELECT
	CAMPAIGN_ID,
	CAMPAIGN_NAME,
	SUM(SPEND) AS TOTAL_SPEND,
	SUM(CLICKS) AS TOTAL_CLICKS,
	SUM(IMPRESSIONS) AS TOTAL_IMPRESSIONS
FROM
	AD_SPEND
GROUP BY
	CAMPAIGN_ID,
	CAMPAIGN_NAME;

SELECT
	*
FROM
	CAMPAIGN_SPEND
ORDER BY
	TOTAL_SPEND DESC;

-- Create Campaign Performance View
CREATE OR REPLACE VIEW CAMPAIGN_PERFORMANCE AS
SELECT
	S.CAMPAIGN_ID,
	S.CAMPAIGN_NAME,
	S.TOTAL_SPEND,
	S.TOTAL_CLICKS,
	S.TOTAL_IMPRESSIONS,
	COALESCE(R.ATTRIBUTED_REVENUE, 0) AS ATTRIBUTED_REVENUE,
	COALESCE(R.ATTRIBUTED_CONVERSIONS, 0) AS ATTRIBUTED_CONVERSIONS
FROM
	CAMPAIGN_SPEND S
	LEFT JOIN LINEAR_CAMPAIGN_REVENUE R ON S.CAMPAIGN_ID = R.CAMPAIGN_ID;

SELECT
	*
FROM
	CAMPAIGN_PERFORMANCE;

/*==========================================================
SECTION 3
Marketing KPIs
==========================================================*/
-- Create Campaign KPIs 
CREATE OR REPLACE VIEW CAMPAIGN_KPIS AS
SELECT
	*,
	ROUND(ATTRIBUTED_REVENUE / NULLIF(TOTAL_SPEND, 0), 2) AS ROAS
FROM
	CAMPAIGN_PERFORMANCE;

SELECT
	CAMPAIGN_NAME,
	TOTAL_SPEND,
	ATTRIBUTED_REVENUE,
	ATTRIBUTED_CONVERSIONS,
	ROAS
FROM
	CAMPAIGN_KPIS
ORDER BY
	ROAS DESC
LIMIT
	10;

CREATE OR REPLACE VIEW CAMPAIGN_KPIS_2 AS
SELECT
	*,
	ROUND(ATTRIBUTED_REVENUE / NULLIF(TOTAL_SPEND, 0), 2) AS ROAS,
	ROUND(
		TOTAL_SPEND / NULLIF(ATTRIBUTED_CONVERSIONS, 0),
		2
	) AS CAC
FROM
	CAMPAIGN_PERFORMANCE;

SELECT
	CAMPAIGN_NAME,
	TOTAL_SPEND,
	ATTRIBUTED_REVENUE,
	ATTRIBUTED_CONVERSIONS,
	ROAS,
	CAC
FROM
	CAMPAIGN_KPIS_2
ORDER BY
	CAC DESC
LIMIT
	10;

