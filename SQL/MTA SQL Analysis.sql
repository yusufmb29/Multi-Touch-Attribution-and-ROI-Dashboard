-- Creating User Journey Table/View
CREATE OR REPLACE VIEW USER_JOURNEY AS
SELECT
	T.USER_ID,
	T.SESSION_ID,
	T.TIMESTAMP,
	T.SOURCE,
	T.MEDIUM,
	T.CAMPAIGN_ID,
	T.CAMPAIGN_NAME,
	T.EVENT_TYPE,
	C.CONVERSION_ID,
	C.CONVERSION_TIMESTAMP,
	C.REVENUE
FROM
	USER_TOUCHPOINTS T
	JOIN CONVERSIONS C ON T.USER_ID = C.USER_ID
WHERE
	T.TIMESTAMP <= C.CONVERSION_TIMESTAMP;

SELECT
	*
FROM
	USER_JOURNEY;

SELECT
	USER_ID,
	SOURCE,
	TIMESTAMP,
	ROW_NUMBER() OVER (
		PARTITION BY
			USER_ID
		ORDER BY
			TIMESTAMP
	) AS TOUCH_ORDER
FROM
	USER_JOURNEY
ORDER BY
	USER_ID,
	TIMESTAMP
LIMIT
	30;

SELECT
	USER_ID,
	SOURCE,
	TIMESTAMP,
	ROW_NUMBER() OVER (
		PARTITION BY
			USER_ID
		ORDER BY
			TIMESTAMP
	) AS TOUCH_ORDER,
	COUNT(*) OVER (
		PARTITION BY
			USER_ID
	) AS TOTAL_TOUCHES
FROM
	USER_JOURNEY
ORDER BY
	USER_ID,
	TIMESTAMP
LIMIT
	30;

/*==========================================================
SECTION 3
First Touch Attribution
==========================================================*/
-- First-Touch Attribution Model
-- Asks "Who introduced the customer?"
CREATE OR REPLACE VIEW FIRST_TOUCH_ATTRIBUTION AS
WITH
	JOURNEY_RANKED AS (
		SELECT
			USER_ID,
			SOURCE,
			MEDIUM,
			CAMPAIGN_ID,
			CAMPAIGN_NAME,
			CONVERSION_ID,
			CONVERSION_TIMESTAMP,
			REVENUE,
			ROW_NUMBER() OVER (
				PARTITION BY
					USER_ID
				ORDER BY
					TIMESTAMP
			) AS TOUCH_ORDER
		FROM
			USER_JOURNEY
	)
SELECT
	*
FROM
	JOURNEY_RANKED
WHERE
	TOUCH_ORDER = 1;

SELECT
	*
FROM
	FIRST_TOUCH_ATTRIBUTION;

SELECT
	COUNT(*)
FROM
	FIRST_TOUCH_ATTRIBUTION;

SELECT
	SOURCE,
	COUNT(*) AS CONVERSIONS,
	SUM(REVENUE) AS ATTRIBUTED_REVENUE
FROM
	FIRST_TOUCH_ATTRIBUTION
GROUP BY
	SOURCE
ORDER BY
	ATTRIBUTED_REVENUE ASC;

