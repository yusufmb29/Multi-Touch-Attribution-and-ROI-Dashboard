-- Dim Date Table
DROP VIEW IF EXISTS DIM_DATE;

CREATE VIEW DIM_DATE AS
SELECT DISTINCT
	DATE,
	EXTRACT(
		YEAR
		FROM
			DATE
	) AS YEAR,
	EXTRACT(
		MONTH
		FROM
			DATE
	) AS MONTH_NO,
	TO_CHAR(DATE, 'Month') AS MONTH_NAME,
	TO_CHAR(DATE, 'Mon') AS MONTH_SHORT,
	EXTRACT(
		QUARTER
		FROM
			DATE
	) AS QUARTER,
	EXTRACT(
		DAY
		FROM
			DATE
	) AS DAY,
	TO_CHAR(DATE, 'Day') AS WEEKDAY_NAME,
	EXTRACT(
		DOW
		FROM
			DATE
	) AS WEEKDAY_NO
FROM
	FACT_ATTRIBUTION
ORDER BY
	DATE;

SELECT
	COUNT(*)
FROM
	DIM_DATE;

-- Fact Attribution View
CREATE OR REPLACE VIEW FACT_ATTRIBUTION AS
-- First Touch
SELECT
	CAMPAIGN_ID,
	CAMPAIGN_NAME,
	CONVERSION_TIMESTAMP::DATE AS DATE,
	'First Touch' AS MODEL_TYPE,
	SUM(REVENUE) AS ATTRIBUTED_REVENUE,
	COUNT(*) AS ATTRIBUTED_CONVERSIONS
FROM
	FIRST_TOUCH_ATTRIBUTION
GROUP BY
	CAMPAIGN_ID,
	CAMPAIGN_NAME,
	CONVERSION_TIMESTAMP::DATE
UNION ALL
-- Last Touch
SELECT
	CAMPAIGN_ID,
	CAMPAIGN_NAME,
	CONVERSION_TIMESTAMP::DATE AS DATE,
	'Last Touch' AS MODEL_TYPE,
	SUM(REVENUE) AS ATTRIBUTED_REVENUE,
	COUNT(*) AS ATTRIBUTED_CONVERSIONS
FROM
	LAST_TOUCH_ATTRIBUTION
GROUP BY
	CAMPAIGN_ID,
	CAMPAIGN_NAME,
	CONVERSION_TIMESTAMP::DATE
UNION ALL
-- Linear
SELECT
	CAMPAIGN_ID,
	CAMPAIGN_NAME,
	CONVERSION_TIMESTAMP::DATE AS DATE,
	'Linear' AS MODEL_TYPE,
	SUM(ATTRIBUTED_REVENUE) AS ATTRIBUTED_REVENUE,
	COUNT(DISTINCT CONVERSION_ID) AS ATTRIBUTED_CONVERSIONS
FROM
	LINEAR_ATTRIBUTION
GROUP BY
	CAMPAIGN_ID,
	CAMPAIGN_NAME,
	CONVERSION_TIMESTAMP::DATE;

SELECT
	*
FROM
	FACT_ATTRIBUTION;

SELECT
	*
FROM
	FACT_ATTRIBUTION
LIMIT
	5;